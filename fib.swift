#!/usr/bin/env swift

import Foundation

var program = [
  0xa9, 0xFF,
  0x8d, 0x02, 0x60,

  0xa9, 0x01,
  0x8d, 0x00, 0x60,
  
  0xa9, 0x01,
  0x8d, 0x01, 0x60,

  0x6d, 0x00, 0x60,
  0x8d, 0x00, 0x60,

  0x4c, 0x0a, 0x80

  ] as [UInt8]


let count = 0x8000 - program.count
var padding = [UInt8](repeating: 0xea, count: count)

program = program + padding

program[0x7ffc] = 0x00 
program[0x7ffd] = 0x80

try? Data(program).write(to: URL(fileURLWithPath:"rom.bin"))